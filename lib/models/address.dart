class Address {
  final String name;
  final String mobile;
  final String email;
  final String addressLine1;
  final String addressLine2;
  final String city;
  final int pin;

  Address({
    required this.name,
    required this.mobile,
    required this.email,
    required this.addressLine1,
    required this.addressLine2,
    required this.city,
    required this.pin,
  });
}
